    private void cmd_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmd_loginActionPerformed
      
        String sql="select * from empdetails where Username=? and Password=?";
        try{
             
            ps=conn.prepareStatement(sql);
            ps.setString(1,txt_username.getText());
            ps.setString(2,txt_password.getText());
            rs=ps.executeQuery();
           
          if(rs.next())
            {
                JOptionPane.showMessageDialog(null,"Username and password is correct");
               rs.close();
      ps.close();
      
               Employee_info s=new Employee_info();
              s.setVisible(true);
            }
            else{
                  JOptionPane.showMessageDialog(null,"Username and password is not_correct");
            }
               
        }
        catch(Exception e)
        {
            System.out.print(e);
        }
        
    }//GEN-LAST:event_cmd_loginActionPerformed

